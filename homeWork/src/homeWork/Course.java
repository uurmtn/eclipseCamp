package homeWork;

public class Course {

	public Course(int id, String name, String instructor, String detail, String studentQuantity) {

		this.id = id;
		this.name = name;
		this.instructor = instructor;
		this.detail = detail;
		this.studentQuantity = studentQuantity;
	}

	int id;
	String name;
	String instructor;
	String detail;
	String studentQuantity;
}
